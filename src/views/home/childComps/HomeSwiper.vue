<template>
  <swiper>
    <swiper-item v-for="item in banners">
      <a :href="item.link">
        <img :src="item.image" @load="swiperimgload">
      </a>
    </swiper-item>
  </swiper>

</template>

<script>
import {Swiper,SwiperItem} from "@/components/common/swiper"

export default {
  name: "HomeSwiper",
  props:{
    banners:{
      type:Array,
      default(){
        return[]
      }
    }
  },
  data(){
    return{
      swiperemit:false
    }
  },
  components:{
    Swiper,
    SwiperItem
  },
 methods:{
    swiperimgload(){
      if(!this.swiperemit){
        this.$emit("swiperImgLoad")
        this.swiperemit=true
      }

    }
  }
}
</script>

